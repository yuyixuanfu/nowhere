def main() -> None:
    data = json.loads(_DATA.read_text(encoding="utf-8"))
    places = data.get("places", {})
    found = 0
    missing: list[str] = []
    for name in places:
        if name in COORDS:
            lat, lon = COORDS[name]
            places[name]["lat"] = round(lat, 4)
            places[name]["lon"] = round(lon, 4)
            found += 1
        else:
            missing.append(name)

    _DATA.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Done: {found}/{len(places)} places geocoded.")
    if missing:
        print(f"Missing ({len(missing)}): {', '.join(missing)}")


if __name__ == "__main__":
    main()
